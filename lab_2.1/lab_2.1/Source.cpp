#include <iostream>
using namespace std;
int main()
{
	double Pi = 4 * atan(1.0); // Число пі
	double a; // вхідний параметр
	double z1; // результат обчислення 1-го виразу
	double z2; // результат обчислення 2-го виразу
	cout << "a = "; cin >> a;
	// Обчислення по першій формулі
	z1 = 2 * pow(sin((3 * Pi) - (2 * a)), 2) * pow(cos((5 * Pi) + (2 * a)), 2);
	// Обчислення по другій формулі
	z2 = 1.0/4 - 1.0/4 * sin((5.0/2 * Pi) - (8 * a));
	cout << endl;
	cout << "z1 = " << z1 << endl;
	cout << "z2 = " << z2 << endl;
	cin.get();
	return 0;
}